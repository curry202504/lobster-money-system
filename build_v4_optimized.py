def h(text, level=1): return doc.add_heading(text, level=level)
def p(text, bold=False, size=None, align=None, color=None, italic=False):
    para = doc.add_paragraph()
    if align: para.alignment = align
    run = para.add_run(text)
    run.font.name = 'Calibri'
    if bold: run.bold = True
    if italic: run.italic = True
    if size: run.font.size = Pt(size)
    if color: run.font.color.rgb = RGBColor(*color)
    return para

def tb(headers, rows, merged=None):
    t = doc.add_table(rows=1+len(rows), cols=len(headers))
    t.style = 'Light Shading Accent 1'; t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, h in enumerate(headers):
        c = t.rows[0].cells[i]; c.text = h
        for para in c.paragraphs:
            para.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for r in para.runs: r.bold = True; r.font.size = Pt(9)
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            c = t.rows[ri+1].cells[ci]; c.text = str(val)
            for para in c.paragraphs:
                for r in para.runs: r.font.size = Pt(9)
    doc.add_paragraph(); return t

def add_img(path, caption, source=""):
    if path and os.path.exists(path):
        try:
            img = Image.open(path)
            w, h = img.size
            ratio = min(1.0, 350 / w) if w > 0 else 1
            para = doc.add_paragraph()
            para.alignment = WD_ALIGN_PARAGRAPH.CENTER
            run = para.add_run()
            run.add_picture(path, width=Cm(ratio * w * 0.035))
        except: pass
    cap = doc.add_paragraph(); cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = cap.add_run('图：' + caption)
    run.font.size = Pt(8); run.font.color.rgb = RGBColor(100,100,100); run.italic = True
    if source:
        s = doc.add_paragraph(); s.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = s.add_run('来源：' + source)
        run.font.size = Pt(7); run.font.color.rgb = RGBColor(150,150,150)

