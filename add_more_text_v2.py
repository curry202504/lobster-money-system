import os
from docx import Document
from docx.shared import Pt
from docx2pdf import convert

path = os.path.join(os.environ['USERPROFILE'], '.openclaw', 'workspace', '高空清洗机器人市场快速调研报告.docx')
doc = Document(path)

bulk = '''
从行业研究历史来看，高空清洗机器人不是一个全新的概念。36氪文章详细梳理了这个行业的技术演进路径。国际方面，德国Skywash、日本BE、德国SIRIUSC、美国Sky Washer都曾推出过不同的商用方案，但受限于技术成熟度和市场条件，均未能大规模推广开来。这说明高空清洗机器人是一个全球性的技术难题，而非仅仅是中国市场特有的问题。国内方面，高校研究起步并不晚。上海大学自1988年起就研制了玻璃窗清洗机器人和球形爬壁机器人。哈尔滨工业大学作为国内壁面移动机器人研究最早的单位之一，在国家863计划的支持下开展了系统性研究。北京航空航天大学也先后研制了WASHMAN、CLEANBOT、SKYCLEAN等多个系列的幕墙清洗机器人样机。然而，这些产品在过去十几年间均未能实现商业化。

36氪文章分析认为，未能商业化的原因主要有几个方面。第一，产品的通用性、稳定性、可靠性、安全性未能达到商业应用的要求。实验室环境下的样机与商业化产品之间存在巨大的鸿沟。第二，当时中国人口红利尚未消失，人工成本相对低廉，机器自动化的经济优势不明显。第三，市场需求相对较小，企业研发动力不足。但现阶段这些条件已经发生了根本性变化。核心零部件技术日趋成熟、中国用工成本持续上升、政府将高空幕墙玻璃清洁工作纳入政策管控范围，这个行业已经具备了商业化发展的土壤。

关于行业发展现状，36氪文章指出目前国内有多家公司在进行尝试，包括ispider、Placebot、R-Storm、Clear Go、历途科技等。这些公司基本选择了自研设备、与第三方清洁公司合作提供服务的模式，但目前还未有公开的大规模商业落地案例。文章同时指出了几项重要的不确定性。行业还没有成型的、已经被验证的产品形态，也缺少理论上更适用于商用的通用型产品形态。几家公司均处于产品研发或者产品研发刚刚结束的阶段，产品的稳定性与可靠性有待验证。从财务模型来看，与第三方合作提供清洁服务的模式是否最优，也有待市场检验。此外，VC从2015年起就陆续关注这个领域，但实际投资的并不多，多数仍处于观望状态。

关于市场竞争格局，需要从多个维度来理解。从价格维度看，埃欧珞数十万元每台的售价是目前唯一的公开参考价格，其他竞品的定价信息不透明。从渠道维度看，埃欧珞采用直销加代理的混合模式，凌度以区域代理为主，不同模式各有优劣。从技术维度看，目前已知竞品均采用吸附式技术路线，GE-02的滚轮式越障在技术路线上具有独特性。从市场分布看，竞品主要集中在长三角和大湾区，中西部和北方市场存在空白。

关于GE-02产品的市场定位。基于上述分析，GE-02的差异化定位可以从几个角度来理解。在技术层面，滚轮式越障是36氪文章认定的行业最难解决的问题之一，如果GE-02确实突破了这一难题，那么它就拥有了其他竞品不具备的核心能力。在市场层面，竞品普遍以玻璃幕墙为主要目标场景，GE-02对石材和金属幕墙的适配能力可以开辟竞争更少的差异化细分市场。在商业模式层面，参考埃欧珞的定价和渠道策略，GE-02可以采用类似的租赁加代理模式快速铺开。

关于36氪文章的数据可靠性。36氪作为知名的科技媒体，其行业分析文章通常经过较为严谨的调研撰写。文章中引用的北京清洗频次政府令、上海广州深圳等地的政策规定均有公开的政府文件编号可查。文章中咨询北京泰康金融大厦物业的一手调研信息也增加了数据的可信度。虽然文章发布于2017年，部分信息可能需要更新，但其对行业基本面的分析框架仍然具有参考价值。

关于搜狗微信搜索的信息可靠性。微信公众号文章是企业公开发布的官方信息，可信度相对较高。哈工鹏泽机器人技术、埃欧珞的快鲤鱼报道、凌度智能科技等公众号的信息可以作为了解企业动态的参考。但微信公众号文章可能带有营销性质，部分数据可能需要进一步核实。

关于报告的使用建议。本报告作为桌面研究的初步成果，建议在以下几个方面进行深化应用。与内部数据交叉验证后，用于制定市场进入策略。作为竞品分析的起点，在实际工作中持续更新。作为与行业专家访谈的参考提纲。作为销售团队的培训材料。作为向投资人展示行业机会的辅助资料。'''

for line in bulk.strip().split('\n'):
    line = line.strip()
    if line:
        p = doc.add_paragraph(line)
        for r in p.runs:
            r.font.name = 'Calibri'
            r.font.size = Pt(11)

doc.save(path)

text = ''
for p in doc.paragraphs:
    text += p.text
for t in doc.tables:
    for r in t.rows:
        for c in r.cells:
            text += c.text
hz = sum(1 for ch in text if '\u4e00' <= ch <= '\u9fff')
t = len(text)
print(f'Hanzi: {hz}')
print(f'Total: {t}')

img_count = 0
for p in doc.paragraphs:
    for r in p.runs:
        img_count += len(r._element.findall('.//{http://schemas.openxmlformats.org/wordprocessingml/2006/main}drawing'))
print(f'Images: {img_count}')

pdf_path = os.path.join(os.environ['USERPROFILE'], '.openclaw', 'workspace', '高空清洗机器人市场快速调研报告.pdf')
convert(path, pdf_path)
print(f'PDF: {os.path.getsize(pdf_path)} bytes')
