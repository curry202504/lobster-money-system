def heading(text, level=1):
    h = doc.add_heading(text, level=level)
    return h

def para(text, bold=False, size=None, align=None, color=None):
    p = doc.add_paragraph()
    if align: p.alignment = align
    run = p.add_run(text)
    run.font.name = 'Calibri'
    if bold: run.bold = True
    if size: run.font.size = Pt(size)
    if color: run.font.color.rgb = RGBColor(*color)
    return p

def table(headers, rows):
    t = doc.add_table(rows=1+len(rows), cols=len(headers))
    t.style = 'Light Shading Accent 1'
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, h in enumerate(headers):
        cell = t.rows[0].cells[i]
        cell.text = h
        for p in cell.paragraphs:
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for run in p.runs:
                run.bold = True; run.font.size = Pt(9)
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            cell = t.rows[ri+1].cells[ci]
            cell.text = str(val)
            for p in cell.paragraphs:
                for run in p.runs:
                    run.font.size = Pt(9)
    doc.add_paragraph()
    return t

def add_img(img_path, caption, source=""):
    if img_path and os.path.exists(img_path):
        try:
            img = Image.open(img_path)
            w, h = img.size
            max_w = 350
            ratio = max_w / w if w > max_w else 1
            p = doc.add_paragraph()
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            run = p.add_run()
            run.add_picture(img_path, width=Cm(ratio * w * 0.035))
        except:
            pass
    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = cap.add_run(f'图：{caption}')
    run.font.size = Pt(8); run.font.color.rgb = RGBColor(100,100,100); run.italic = True
    if source:
        s = doc.add_paragraph()
        s.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = s.add_run(f'来源：{source}')
        run.font.size = Pt(7); run.font.color.rgb = RGBColor(150,150,150)

